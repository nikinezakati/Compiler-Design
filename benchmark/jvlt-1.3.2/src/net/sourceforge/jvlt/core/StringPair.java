package net.sourceforge.jvlt.core;

public class StringPair {
	private final String first;
	private final String second;

	public StringPair(String first, String second) {
		this.first = first;
		this.second = second;
	}

	public String getFirst() {
		return first;
	}

	public String getSecond() {
		return second;
	}
}
